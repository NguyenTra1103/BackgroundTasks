package com.example.backgroundtasks;

public class User {
    public Integer id;
    public String name;
    public String email;
    public String gender;
    public String status;

    public User(Integer id, String name, String email, String gender, String status) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.gender = gender;
        this.status = status;
    }
}
